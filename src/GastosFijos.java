public class GastosFijos {
    private String nombre;
    private double costo;

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setCosto(double costo) {
        this.costo = costo;
    }
    public String getNombre() {
        return nombre;
    }
    public double getCosto() {
        return costo;
    }


}
